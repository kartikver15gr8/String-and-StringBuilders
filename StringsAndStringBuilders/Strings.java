package com.StringsAndStringBuilders;

public class Strings {
    public static void main(String[] args) {
        // As we store data as different data types
        // String is also a data type used to store a "String" of "Characters"

        int a = 10;     // Here I assigned reference variable 'a' to an 'Integer' Data type
        System.out.println(a);
        String b = "10"; // Here I assigned reference variable 'b' to a 'String' Data type
        System.out.println(b);
        String name = "Kartikey Verma";
        System.out.println(name.charAt(5));
        System.out.println(name);

    }
}
